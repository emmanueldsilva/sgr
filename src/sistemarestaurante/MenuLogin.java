    private void entrarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_entrarButtonActionPerformed
        try {
            final Usuario usuario = new ControleUsuario()
                    .buscaUsuario(loginText.getText(), new String(senhaField.getPassword()));

            setVisible(false);
            FrameCentral programa;
            programa = FrameCentral.getInstancia();
            programa.setVisible(true);
            programa.instanciaMesas();
            programa.setUsuarioLogado(usuario);
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Erro!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_entrarButtonActionPerformed


